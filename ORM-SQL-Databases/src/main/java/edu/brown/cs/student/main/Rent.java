package edu.brown.cs.student.main;

public class Rent {
  String fit;
  String user_id;
  String item_id;
  String rating;
  String rented_for;
  String category;
  String size;
  String id;

  public Rent(String fit, String user_id, String item_id, String rating, String rented_for, String category, String size, String id) {
   this.fit = fit;
   this.user_id = user_id;
   this.item_id = item_id;
   this.rating = rating;
   this.rented_for = rented_for;
   this.category = category;
   this.size = size;
   this.id = id;
  }




}
